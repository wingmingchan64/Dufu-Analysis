function 替換詩陣列文字( 
	array &$詩陣列, string $坐標, string $文字 ) : bool
{
	$默詩碼 = mb_substr( $坐標, 1, 4 );
	
	if( 是組詩( $默詩碼 ) )
	{
		$首碼 = 提取首碼( $坐標 );
		$默詩碼 = $默詩碼 . '-' . $首碼;
	}
	if( 是含範圍字碼完整坐標( $坐標 ) )
	{
		$路徑陣列s = 含範圍字碼完整坐標轉換成列陣路徑( $坐標 );
	}
	else
	{
		$路徑陣列s = array( 完整坐標轉換成列陣路徑( $坐標 ) );
	}
	$size = sizeof( $路徑陣列s );
	
	for( $i = 0; $i < $size - 1; $i++ )
	{
		替換路徑文字( $詩陣列, $默詩碼, $路徑陣列s[ $i ], 空語鏈 );
	}
	替換路徑文字(
		$詩陣列, $默詩碼, $路徑陣列s[ $size - 1 ], $文字 );
	
	return true;
}
