0126 贈特進汝陽王二十二韻

特進羣公表。天人夙德升。霜蹄千里駿。風翮九霄鵬。服禮求毫髮。惟忠忘寢興。聖情常有眷。朝退若無憑。仙醴來浮蟻。奇毛或賜鷹。清關塵不雜。中使日相乘。晚節嬉遊簡。平居孝義稱。自多親棣萼。誰敢問山陵。學業醇儒富。辭華哲匠能。筆飛鸞聳立。章罷鳳騫騰。精理通談笑。忘形向友朋。寸長堪繾綣。一諾豈驕矜。已忝歸曹植。何知對李膺。招要恩屢至。崇重力難勝。披霧初歡夕。高秋爽氣澄。樽罍臨極浦。鳧鴈宿張燈。花月窮遊宴。炎天避鬱蒸。硯寒金井水。簷動玉壺冰。瓢飲唯三逕。巖栖在百層。且持蠡測海。況挹酒如澠。鴻寶寧全祕。丹梯庶可凌。淮王門有客。終不媿孫登。

