0042 遊龍門奉先寺

已從招提遊。更宿招提境。陰壑生虚籟。月林散清影。天闕象緯逼。雲臥衣裳冷。欲覺聞晨鍾。令人發深省。

0048 過宋員外之問舊莊

宋公舊池館。零落首陽阿。枉道祗從入。吟詩許更過。淹留問耆老。寂寞向山河。更識將軍樹。悲風日暮多。

